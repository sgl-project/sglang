_running = True


def _db_init():
    os.makedirs(os.path.dirname(DB), exist_ok=True)
    conn = sqlite3.connect(DB, timeout=10)
    conn.execute("PRAGMA journal_mode=WAL")
    conn.execute("""CREATE TABLE IF NOT EXISTS licensing_log (
        id INTEGER PRIMARY KEY, file_path TEXT, action TEXT,
        fingerprint TEXT, status TEXT, notes TEXT,
        ts DATETIME DEFAULT CURRENT_TIMESTAMP)""")
    conn.commit(); conn.close()


def _log(path: str, action: str, fingerprint: str, status: str, notes: str = ""):
    conn = sqlite3.connect(DB, timeout=10)
    conn.execute("""INSERT INTO licensing_log (file_path,action,fingerprint,status,notes)
        VALUES (?,?,?,?,?)""", (path, action, fingerprint, status, notes[:300]))
    conn.commit(); conn.close()


def _fingerprint(content: str) -> str:
    return hashlib.sha256(content.encode()).hexdigest()[:16]


def _stamp_markdown(path: str) -> str:
    with open(path, "r", errors="ignore") as f:
        content = f.read()

    if "SPDX-License-Identifier" in content:
        return "already_stamped"

    stamped = COPYRIGHT_HEADER + "\n\n" + content
    if not DRY_RUN:
        with open(path, "w") as f:
            f.write(stamped)
    return "stamped"


def _write_license_file(target_dir: str) -> str:
    path = os.path.join(target_dir, "LICENSE")
    if os.path.exists(path):
        return "exists"
    if not DRY_RUN:
        with open(path, "w") as f:
            f.write(LICENSE_BODY)
    return "created"


def _write_copyright_notice(target_dir: str) -> str:
    path = os.path.join(target_dir, "COPYRIGHT.txt")
    notice = f"Copyright (c) {YEAR} {OPERATOR} (ABN {ABN}) — Sole Trader\n"
    notice += f"Website: https://fractalmesh.net\n"
    notice += f"All rights reserved.\n"
    if not DRY_RUN:
        with open(path, "w") as f:
            f.write(notice)
    return "written"


def run_cycle():
    ts    = datetime.utcnow().isoformat()
    dist  = os.path.join(REPO, "fractalmesh", "dist")
    print(f"[fm-licensing] {ts} | dry={DRY_RUN}")

    # Stamp all dist/ markdown files
    stamped = 0
    if os.path.exists(dist):
        for fname in os.listdir(dist):
            if not fname.endswith(".md"):
                continue
            path   = os.path.join(dist, fname)
            status = _stamp_markdown(path)
            fp     = _fingerprint(fname)
            _log(path, "stamp_copyright", fp, status)
            print(f"   {fname}: {status}")
            stamped += 1

    # Write LICENSE to dist/
    if os.path.exists(dist):
        s = _write_license_file(dist)
        _log(os.path.join(dist, "LICENSE"), "write_license", "—", s)
        print(f"   LICENSE: {s}")

    # Write COPYRIGHT.txt to dist/
    if os.path.exists(dist):
        s = _write_copyright_notice(dist)
        _log(os.path.join(dist, "COPYRIGHT.txt"), "write_copyright", "—", s)
        print(f"   COPYRIGHT.txt: {s}")

    # Patent/IP notice to sovereign.db pulse log
    conn = sqlite3.connect(DB, timeout=10)
    conn.execute("""INSERT INTO pulse_log (source,event,priority) VALUES (?,?,?)
        """, ("fm-licensing",
              f"IP_NOTICE: φ-harmonic+Enochian+4A reserved for patent | {YEAR}",
              1.0))
    conn.commit(); conn.close()
    print(f"   Stamped {stamped} files | LICENSE + COPYRIGHT written")


def _sigterm(s, f):
    global _running
    _running = False


if __name__ == "__main__":
    signal.signal(signal.SIGTERM, _sigterm)
    signal.signal(signal.SIGINT,  _sigterm)
    _db_init()
    print(f"[fm-licensing] Active | interval={INTERVAL}s | dry={DRY_RUN}")
    while _running:
        try:
            run_cycle()
        except Exception as e:
            print(f"[fm-licensing] ERR {e}")
        for _ in range(INTERVAL):
            if not _running: break
            time.sleep(1)
    print("[fm-licensing] Stopped.")
