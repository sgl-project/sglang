def _db_init():
    os.makedirs(os.path.dirname(DB), exist_ok=True)
    conn = sqlite3.connect(DB, timeout=10)
    conn.execute("PRAGMA journal_mode=WAL")
    conn.execute("""CREATE TABLE IF NOT EXISTS smart_contracts (
        id INTEGER PRIMARY KEY, contract_type TEXT, name TEXT,
        filename TEXT, source_hash TEXT UNIQUE, network TEXT DEFAULT 'undeployed',
        deployed_addr TEXT, phi_score REAL,
        ts DATETIME DEFAULT CURRENT_TIMESTAMP)""")
    conn.commit(); conn.close()


def _log_contract(ctype: str, name: str, filename: str,
                  src_hash: str, phi: float):
    conn = sqlite3.connect(DB, timeout=10)
    try:
        conn.execute("""INSERT INTO smart_contracts
            (contract_type,name,filename,source_hash,phi_score)
            VALUES (?,?,?,?,?)""",
            (ctype, name, filename, src_hash, phi))
        conn.commit()
    except sqlite3.IntegrityError:
        pass
    finally:
        conn.close()


def _write_contract(filename: str, source: str) -> str:
    os.makedirs(DIST, exist_ok=True)
    path = os.path.join(DIST, filename)
    if not DRY_RUN:
        with open(path, "w") as f:
            f.write(source)
    return hashlib.sha256(source.encode()).hexdigest()[:16]


