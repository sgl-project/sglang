def _erc20_template(name: str, symbol: str, total_supply: int,
                    owner_addr: str = "0x0000000000000000000000000000000000000001") -> str:
    return f"""// SPDX-License-Identifier: LicenseRef-FractalMesh-Sovereign
// Copyright (c) {datetime.utcnow().year} {OPERATOR} (ABN {ABN}) — Sole Trader
// Generated by FractalMesh Smart Contracts Agent v2.0.0
// WARNING: Review with a qualified Solidity auditor before deployment.

pragma solidity ^0.8.20;

contract {re.sub(r'[^A-Za-z0-9]', '', name)}Token {{
    string  public name     = "{name}";
    string  public symbol   = "{symbol}";
    uint8   public decimals = 18;
    uint256 public totalSupply;
    address public owner;

    mapping(address => uint256) public balanceOf;
    mapping(address => mapping(address => uint256)) public allowance;

    event Transfer(address indexed from, address indexed to, uint256 value);
    event Approval(address indexed owner, address indexed spender, uint256 value);

    constructor() {{
        owner       = {owner_addr};
        totalSupply = {total_supply} * 10**uint256(decimals);
        balanceOf[owner] = totalSupply;
        emit Transfer(address(0), owner, totalSupply);
    }}

    modifier onlyOwner() {{
        require(msg.sender == owner, "Not owner");
        _;
    }}

    function transfer(address to, uint256 value) external returns (bool) {{
        require(balanceOf[msg.sender] >= value, "Insufficient");
        balanceOf[msg.sender] -= value;
        balanceOf[to]         += value;
        emit Transfer(msg.sender, to, value);
        return true;
    }}

    function approve(address spender, uint256 value) external returns (bool) {{
        allowance[msg.sender][spender] = value;
        emit Approval(msg.sender, spender, value);
        return true;
    }}

    function transferFrom(address from, address to, uint256 value) external returns (bool) {{
        require(balanceOf[from] >= value, "Insufficient");
        require(allowance[from][msg.sender] >= value, "Allowance");
        balanceOf[from]              -= value;
        allowance[from][msg.sender]  -= value;
        balanceOf[to]                += value;
        emit Transfer(from, to, value);
        return true;
    }}
}}
"""


def _royalty_split_template(splits: list) -> str:
    """splits = [(address, bps)] where bps sum to 10000."""
    payees_init = "\n        ".join(
        f'payees.push({addr}); shares.push({bps});' for addr, bps in splits)
    total_bps = sum(bps for _, bps in splits)
    return f"""// SPDX-License-Identifier: LicenseRef-FractalMesh-Sovereign
// Copyright (c) {datetime.utcnow().year} {OPERATOR} (ABN {ABN}) — Sole Trader
// Royalty split: {total_bps} BPS total across {len(splits)} payees
// Generated by FractalMesh Smart Contracts Agent v2.0.0

pragma solidity ^0.8.20;

contract FractalRoyaltySplit {{
    address[] public payees;
    uint256[] public shares;   // basis points (1/100 of 1%)
    uint256   public totalBps;

    event PaymentReleased(address indexed payee, uint256 amount);

    constructor() {{
        {payees_init}
        totalBps = {total_bps};
    }}

    receive() external payable {{
        uint256 total = msg.value;
        for (uint i = 0; i < payees.length; i++) {{
            uint256 amt = (total * shares[i]) / totalBps;
            if (amt > 0) {{
                (bool ok,) = payees[i].call{{value: amt}}("");
                require(ok, "Transfer failed");
                emit PaymentReleased(payees[i], amt);
            }}
        }}
    }}
}}
"""


def _payment_link_template(recipient: str, amount_wei: int, memo: str) -> str:
    return f"""// SPDX-License-Identifier: LicenseRef-FractalMesh-Sovereign
// Simple payment forwarder — {memo}
// Generated by FractalMesh Smart Contracts Agent v2.0.0

pragma solidity ^0.8.20;

contract FractalPayment {{
    address payable public recipient = payable({recipient});
    uint256 public amount            = {amount_wei};
    string  public memo              = "{memo}";
    bool    public paid;

    event Paid(address indexed sender, uint256 amount);

    function pay() external payable {{
        require(!paid, "Already paid");
        require(msg.value >= amount, "Insufficient");
        paid = true;
        recipient.transfer(msg.value);
        emit Paid(msg.sender, msg.value);
    }}
}}
"""


