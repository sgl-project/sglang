/*
LeetCode 178: Rank Scores
难度: ⭐⭐ Medium
标签: Database, Window Function

题目描述:
编写一个 SQL 查询来实现分数排名。
如果两个分数相同，则两个分数排名（Rank）相同。
请注意，平分后的下一个名次应该是下一个连续的整数值。
换句话说，名次之间不应该有"间隔"。

示例:
输入: Scores 表
+----+-------+
| Id | Score |
+----+-------+
| 1  | 3.50  |
| 2  | 3.65  |
| 3  | 4.00  |
| 4  | 3.85  |
| 5  | 4.00  |
| 6  | 3.65  |
+----+-------+

输出:
+-------+------+
| Score | Rank |
+-------+------+
| 4.00  | 1    |
| 4.00  | 1    |
| 3.85  | 2    |
| 3.65  | 3    |
| 3.65  | 3    |
| 3.50  | 4    |
+-------+------+

核心思路:
1. 使用窗口函数 DENSE_RANK() 计算排名
   - DENSE_RANK(): 相同值相同排名，且排名连续（1,1,2,3,3,4）
   - RANK(): 相同值相同排名，但排名不连续（1,1,3,4,4,6）
   - ROW_NUMBER(): 即使值相同也不同排名（1,2,3,4,5,6）
2. 按 Score DESC 排序（从高到低）
3. 外层查询也需要 ORDER BY Score DESC 确保结果排序

时间复杂度: O(n log n) - 排序
空间复杂度: O(n) - 窗口函数需要存储排序结果
*/

-- ========================================
-- 标准 SQL 实现（推荐，MySQL 8.0+）
-- ========================================
SELECT 
    Score,
    DENSE_RANK() OVER (ORDER BY Score DESC) AS `Rank`
FROM 
    Scores
ORDER BY 
    Score DESC;

-- ========================================
-- 变体 1: 如果数据库不支持窗口函数（MySQL 5.7 及以下）
-- ========================================
SELECT 
    s.Score,
    (SELECT COUNT(DISTINCT t.Score) 
     FROM Scores t 
     WHERE t.Score >= s.Score) AS `Rank`
FROM 
    Scores s
ORDER BY 
    s.Score DESC;

-- ========================================
-- 常见错误
-- ========================================

-- ❌ 错误 1: 使用 RANK() 而不是 DENSE_RANK()
-- SELECT Score, RANK() OVER (ORDER BY Score DESC) AS `Rank`
-- FROM Scores;
-- 错误原因: RANK() 会产生不连续的排名 [1,1,3,4,4,6]，不符合要求

-- ❌ 错误 2: 使用 ROW_NUMBER() 而不是 DENSE_RANK()
-- SELECT Score, ROW_NUMBER() OVER (ORDER BY Score DESC) AS `Rank`
-- FROM Scores;
-- 错误原因: ROW_NUMBER() 会给每个记录不同排名，即使分数相同

-- ❌ 错误 3: 排序方向错误
-- SELECT Score, DENSE_RANK() OVER (ORDER BY Score ASC) AS `Rank`
-- FROM Scores;
-- 错误原因: 题目要求从高到低，应该用 DESC

-- ❌ 错误 4: MySQL 中 Rank 关键字没有用反引号
-- SELECT Score, DENSE_RANK() OVER (ORDER BY Score DESC) AS Rank
-- FROM Scores;
-- 错误原因: Rank 是 MySQL 的保留关键字，需要用反引号包裹

