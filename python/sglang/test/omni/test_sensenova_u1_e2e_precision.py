"""External SenseNova U1 e2e precision check.

This test is intentionally skipped by default because it requires a running
omni server and a reference image generated by the official/native pipeline.
It exercises the public HTTP path so regressions across the SRT engine, omni
orchestrator, and multimodal_gen image generation path are caught together.
"""

import base64
import json
import math
import os
import unittest
from io import BytesIO
from urllib.error import HTTPError
from urllib.request import Request, urlopen

import numpy as np
from PIL import Image

RUN_ENV = "SGLANG_RUN_OMNI_U1_E2E_PRECISION"
REFERENCE_ENV = "SGLANG_OMNI_U1_REFERENCE_IMAGE"


@unittest.skipUnless(
    os.getenv(RUN_ENV) == "1" and os.getenv(REFERENCE_ENV),
    f"set {RUN_ENV}=1 and {REFERENCE_ENV} to run SenseNova U1 e2e precision",
)
class TestSenseNovaU1E2EPrecision(unittest.TestCase):
    def test_multiturn_interleave_edit_matches_reference_image(self):
        base_url = os.getenv("SGLANG_OMNI_U1_BASE_URL", "http://127.0.0.1:30000")
        model = os.getenv("SGLANG_OMNI_U1_MODEL", "sensenova-u1")
        sampling_params = {
            "width": _env_int("SGLANG_OMNI_U1_WIDTH", 1024),
            "height": _env_int("SGLANG_OMNI_U1_HEIGHT", 1024),
            "num_steps": _env_int("SGLANG_OMNI_U1_STEPS", 20),
            "seed": _env_int("SGLANG_OMNI_U1_SEED", 7),
        }
        session_id = None
        try:
            first = _post_json(
                base_url,
                "/v1/omni/generate",
                {
                    "model": model,
                    "task": "interleave",
                    "keep_session": True,
                    "messages": [
                        {
                            "type": "text",
                            "text": (
                                "Create a centered product-style image of a blue "
                                "ceramic mug on a plain light background."
                            ),
                        }
                    ],
                    "sampling_params": sampling_params,
                },
            )
            session_id = first["session"]["id"]
            self.assertIsNotNone(_last_image(first))

            second = _post_json(
                base_url,
                "/v1/omni/generate",
                {
                    "model": model,
                    "task": "interleave",
                    "session_id": session_id,
                    "messages": [
                        {
                            "type": "text",
                            "text": (
                                "Revise the previous image: change the mug to red "
                                "and add one small white star on the front."
                            ),
                        }
                    ],
                    "sampling_params": sampling_params,
                },
            )
            actual = _last_image(second)
            reference = Image.open(os.environ[REFERENCE_ENV]).convert("RGB")
            metrics = _compare_images(actual, reference)

            output_path = os.getenv("SGLANG_OMNI_U1_OUTPUT_IMAGE")
            if output_path:
                actual.save(output_path)

            self.assertGreaterEqual(
                metrics["psnr"],
                _env_float("SGLANG_OMNI_U1_MIN_PSNR", 45.0),
                metrics,
            )
            self.assertLessEqual(
                metrics["mae"],
                _env_float("SGLANG_OMNI_U1_MAX_MAE", 1.5),
                metrics,
            )
            self.assertGreaterEqual(
                metrics["cosine"],
                _env_float("SGLANG_OMNI_U1_MIN_COSINE", 0.999),
                metrics,
            )
        finally:
            if session_id is not None:
                _post_json(
                    base_url,
                    "/v1/omni/generate",
                    {"action": "close_session", "session_id": session_id},
                    raise_on_error=False,
                )


def _post_json(
    base_url: str,
    path: str,
    payload: dict,
    *,
    raise_on_error: bool = True,
) -> dict:
    request = Request(
        f"{base_url.rstrip('/')}{path}",
        data=json.dumps(payload).encode("utf-8"),
        headers={"Content-Type": "application/json"},
        method="POST",
    )
    try:
        with urlopen(
            request,
            timeout=_env_float("SGLANG_OMNI_U1_HTTP_TIMEOUT", 600.0),
        ) as response:
            return json.loads(response.read().decode("utf-8"))
    except HTTPError as exc:
        if not raise_on_error:
            return {}
        body = exc.read().decode("utf-8", errors="replace")
        raise AssertionError(f"omni request failed: {exc.code} {body}") from exc


def _last_image(payload: dict) -> Image.Image:
    for segment in reversed(payload.get("segments", [])):
        if segment.get("type") == "image":
            return _decode_image(segment.get("image"))
    raise AssertionError(f"omni response did not contain an image: {payload}")


def _decode_image(payload) -> Image.Image:
    if isinstance(payload, dict):
        payload = payload.get("b64_json") or payload.get("data")
    if not isinstance(payload, str):
        raise AssertionError(f"unsupported image payload: {type(payload).__name__}")
    if "," in payload and payload.lstrip().startswith("data:"):
        payload = payload.split(",", 1)[1]
    return Image.open(BytesIO(base64.b64decode(payload))).convert("RGB")


def _compare_images(actual: Image.Image, reference: Image.Image) -> dict[str, float]:
    if actual.size != reference.size:
        raise AssertionError(f"image size mismatch: {actual.size} != {reference.size}")
    actual_arr = np.asarray(actual, dtype=np.float32) / 255.0
    reference_arr = np.asarray(reference, dtype=np.float32) / 255.0
    diff = actual_arr - reference_arr
    mse = float(np.mean(diff * diff))
    mae = float(np.mean(np.abs(diff)) * 255.0)
    psnr = math.inf if mse == 0.0 else float(20.0 * math.log10(1.0 / math.sqrt(mse)))
    actual_flat = actual_arr.reshape(-1)
    reference_flat = reference_arr.reshape(-1)
    cosine = float(
        np.dot(actual_flat, reference_flat)
        / (np.linalg.norm(actual_flat) * np.linalg.norm(reference_flat))
    )
    return {"psnr": psnr, "mae": mae, "cosine": cosine}


def _env_int(name: str, default: int) -> int:
    return int(os.getenv(name, str(default)))


def _env_float(name: str, default: float) -> float:
    return float(os.getenv(name, str(default)))


if __name__ == "__main__":
    unittest.main()
