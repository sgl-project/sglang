@_m
def _lora_router_enabled(self) -> bool:
    return bool(self.lora_router_pool) and self.lora_router_mode in (
        "token_interval",
        "prefill_decode",
        "prefill_decode_ba_interval",
        "route_decode",
    )

@_m
def _mark_lora_reuse_request(self, req: Req) -> None:
    req.skip_radix_cache_insert = True
    req.lora_router_kv_reuse = True

@_m
def _get_route_decode_params(self, req: Req) -> Dict[str, Any]:
    custom_params = getattr(req.sampling_params, "custom_params", None)
    if not isinstance(custom_params, dict):
        return {}
    params = custom_params.get("lora_router")
    if not isinstance(params, dict):
        return {}
    if params.get("mode") != "route_decode":
        return {}
    return params

@_m
def _maybe_mark_route_decode_request(self, req: Req) -> None:
    if not self._get_route_decode_params(req):
        return
    req.lora_router_route_decode_pending = True
    self._mark_lora_reuse_request(req)

@_m
def _decode_route_output_text(self, req: Req) -> str:
    token_ids = list(req.output_ids)
    if not token_ids or self.tokenizer is None:
        return ""
    try:
        return self.tokenizer.decode(token_ids, skip_special_tokens=True)
    except TypeError:
        return self.tokenizer.decode(token_ids)

@staticmethod
@_m
def _strip_route_candidate(value: str) -> str:
    return value.strip().strip("`'\".,;:()[]{}<>").lower()

@_m
def _parse_route_decode_output(
    self, text: str, params: Dict[str, Any]
) -> Optional[str]:
    base_route_id = str(params.get("base_route_id") or "base_model")
    route_to_adapter = params.get("route_to_adapter") or {}
    if not isinstance(route_to_adapter, dict):
        route_to_adapter = {}
    known_routes = [base_route_id] + [str(route_id) for route_id in route_to_adapter]
    route_by_lower = {route_id.lower(): route_id for route_id in known_routes}
    lower = text.lower()

    explicit = re.search(r"model_id\s*=\s*([A-Za-z0-9_.:/-]+)", lower)
    if explicit:
        candidate = self._strip_route_candidate(explicit.group(1))
        return route_by_lower.get(candidate)

    stripped = self._strip_route_candidate(lower)
    if stripped in route_by_lower:
        return route_by_lower[stripped]

    mentions = []
    for route_lower, route_id in route_by_lower.items():
        pattern = rf"(?<![A-Za-z0-9_.:/-]){re.escape(route_lower)}(?![A-Za-z0-9_.:/-])"
        if re.search(pattern, lower):
            mentions.append(route_id)
    unique_mentions = list(dict.fromkeys(mentions))
    if len(unique_mentions) == 1:
        return unique_mentions[0]
    return None

@_m
def _route_decode_signal_override(
    self, route_id: str, params: Dict[str, Any]
) -> Optional[str]:
    """Weighted metadata guardrail (alpha RouterHarness.apply_guardrail parity).

    Runs the weighted library router regardless of the LLM's pick. A library
    SPECIALIST decision (priority + strong*100 + positive*10 - negative*250,
    with NEGATIVE signals able to veto noisy keyword matches) OVERRIDES the LLM
    route — matching alpha, where the library wins unless it returns L0. When
    the library returns L0 (default/ambiguous/general-prefix), the LLM's route
    is kept (return None). Falls back to the legacy strong+positive single-hit
    path only if route_library is absent (older configure payloads), and that
    legacy path still only rescues an L0 LLM pick.
    """
    base_route_id = str(params.get("base_route_id") or "base_model")

    user_text = params.get("user_text")
    route_to_adapter = params.get("route_to_adapter") or {}
    if not isinstance(user_text, str) or not user_text.strip():
        return None
    if not isinstance(route_to_adapter, dict):
        route_to_adapter = {}

    route_library = params.get("route_library")
    if isinstance(route_library, dict) and route_library:
        from . import _mol_routing
        selected, decision = _mol_routing.route_by_library(user_text, route_library)
        if (selected and selected != base_route_id and selected in route_to_adapter
                and selected != route_id):
            if logger is not None:
                logger.info(
                    "lora_router route_by_library override route=%s -> %s decision=%s",
                    route_id, selected, decision,
                )
            return selected
        return None

    # --- legacy fallback: strong+positive single-hit, L0-rescue only ---
    if route_id != base_route_id:
        return None
    route_signals = params.get("route_signals")
    if not isinstance(route_signals, dict):
        return None
    lower = user_text.lower()
    general_prefixes = (
        "translate ",
        "rewrite ",
        "proofread ",
        "explain ",
        "summarize ",
        "compare ",
        "what is ",
        "why does ",
        "give me an overview",
    )
    if lower.strip().startswith(general_prefixes):
        return None
    matches = []
    for candidate_route in route_to_adapter:
        signals = route_signals.get(candidate_route) or []
        if not isinstance(signals, list):
            continue
        for signal in signals:
            if not isinstance(signal, str):
                continue
            normalized = signal.strip().lower()
            if normalized and normalized in lower:
                matches.append(str(candidate_route))
                break

    unique_matches = list(dict.fromkeys(matches))
    if len(unique_matches) == 1:
        return unique_matches[0]
    return None

@_m
def _route_decode_deterministic_override(
    self, route_id: Optional[str], params: Dict[str, Any]
) -> Optional[str]:
    route_to_adapter = params.get("route_to_adapter") or {}
    if not isinstance(route_to_adapter, dict):
        route_to_adapter = {}

    deterministic_route = params.get("deterministic_route")
    if deterministic_route is None:
        return None
    deterministic_route = str(deterministic_route)
    base_route_id = str(params.get("base_route_id") or "base_model")
    if deterministic_route == base_route_id or deterministic_route in route_to_adapter:
        if deterministic_route != route_id:
            return deterministic_route
    return None

@_m
def _route_decode_target(
    self,
    route_id: str,
    params: Dict[str, Any],
    current_lora_id: Optional[str],
) -> Tuple[Optional[str], str]:
    base_route_id = str(params.get("base_route_id") or "base_model")
    route_to_adapter = params.get("route_to_adapter") or {}
    if not isinstance(route_to_adapter, dict):
        route_to_adapter = {}
    if route_id == base_route_id:
        adapter_name = str(
            params.get("base_route_adapter")
            or route_to_adapter.get(route_id)
            or base_route_id
        )
    else:
        adapter_name = str(route_to_adapter.get(route_id, route_id))
    for name, lora_id in zip(self.lora_router_names, self.lora_router_pool):
        if name == adapter_name:
            return lora_id, adapter_name

    logger.warning(
        "route_decode route %s mapped to adapter %s, but adapter is not in "
        "configured lora_pool=%s; falling back to the current request adapter",
        route_id,
        adapter_name,
        self.lora_router_names,
    )
    return current_lora_id, adapter_name

