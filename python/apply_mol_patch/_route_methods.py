@_m
def _release_route_decode_trimmed_kv(
    self, req: Req, keep_prefix_token_count: int, old_kv_allocated_len: int
) -> None:
    if req.req_pool_idx is None or old_kv_allocated_len <= keep_prefix_token_count:
        return
    page_size = max(1, int(getattr(self, "page_size", 1) or 1))
    release_start = keep_prefix_token_count
    if page_size > 1:
        release_start = ((release_start + page_size - 1) // page_size) * page_size
    if old_kv_allocated_len <= release_start:
        req.lora_router_stale_kv_end = 0
        return
    indices_to_free = self.tree_cache.req_to_token_pool.req_to_token[
        req.req_pool_idx, release_start:old_kv_allocated_len
    ]
    if indices_to_free.numel() == 0:
        return
    self.tree_cache.token_to_kv_pool_allocator.free(indices_to_free)
    req.lora_router_stale_kv_end = 0
    logger.info(
        "lora_router route_decode released trimmed kv request_id=%s "
        "keep_prefix=%d release_start=%d old_kv_allocated_len=%d "
        "page_size=%d released=%d",
        req.rid,
        keep_prefix_token_count,
        release_start,
        old_kv_allocated_len,
        page_size,
        int(indices_to_free.numel()),
    )

@_m
def _route_decode_reprefill_enabled(self) -> bool:
    """Toggle for the compromise reprefill fix (A/B against the in-place path).

    MOL_ROUTE_REPREFILL_COSPECIALIST=1 (default): when a route would co-locate
    >=2 specialists, retract+reprefill the req instead of switching mid-decode.
    =0: disable the fix, falling back to the in-place KV-reuse switch (the
    known-garble path), so the same deployment can be A/B'd by relaunching.
    """
    import os as _os
    return _os.environ.get("MOL_ROUTE_REPREFILL_COSPECIALIST", "1") not in (
        "0", "false", "False",
    )


@_m
def _route_decode_would_cause_cospecialist(
    self, batch: ScheduleBatch, index: int, req: Req, target_lora_id: Optional[str]
) -> bool:
    """True if switching req at `index` to `target_lora_id` leaves >=2 distinct
    specialists co-resident in the decode batch.

    This is the exact garble trigger (confirmed: garble IFF conc>=2 AND >=2
    distinct specialists co-resident AND one switches mid-decode). When True,
    the in-place KV-reuse switch would corrupt per-slot weight_indices; the
    caller must instead retract+reprefill this req on the target adapter so it
    never decodes in the same batch as a different specialist.
    """
    if target_lora_id is None or target_lora_id == req.lora_id:
        return False
    specialists = set()
    for i, other in enumerate(batch.reqs):
        if i == index:
            continue
        if other.finished() or other.is_retracted:
            continue
        specialists.add(other.lora_id)
    specialists.add(target_lora_id)
    return len(specialists) >= 2


@_m
def _route_decode_reprefill_req(
    self, batch: ScheduleBatch, index: int, req: Req, route_id: str,
    target_lora_id: Optional[str], adapter_name: str, params: Dict[str, Any],
    decision: str, router_text: str,
) -> bool:
    """Retract req from the running decode batch and re-queue it for a fresh
    full prefill on the target adapter.

    Used by the compromise fix when _route_decode_would_cause_cospecialist is
    True. Instead of switching lora_id mid-decode in a batch that already hosts
    a different specialist (which mis-maps per-slot weight_indices), we drop the
    req from this batch and let the normal scheduler loop re-prefill it on the
    target adapter in its own (single-specialist) batch.

    Steps mirror base sglang's retract path (release_req + reset_for_retract +
    filter_batch + re-queue) so we reuse proven, invariant-checked primitives
    rather than hand-editing batch tensors.
    """
    # Capture the FULL router prompt before any trim. enable_kv_reuse keeps the
    # whole router prompt as the specialist prefix, so re-prefilling the same
    # token ids on the target adapter preserves the specialist's expected input.
    full_input_ids = array("q", req.origin_input_ids)
    full_input_ids_unpadded = array("q", req.origin_input_ids_unpadded)

    router_tokens_removed = len(req.output_ids)
    original_prompt_tokens = len(req.origin_input_ids)
    decode_tokens = max(0, int(params.get("decode_tokens") or 4))

    # Restore the full prompt + reset generation state so the re-prefill
    # regenerates from scratch on the target adapter.
    req.origin_input_ids = full_input_ids
    req.origin_input_ids_unpadded = full_input_ids_unpadded
    del req.output_ids[:]
    req.full_untruncated_fill_ids = array("q")
    req.fill_len = 0
    req.send_token_offset = 0
    req.send_decode_id_offset = 0
    req.send_output_token_logprobs_offset = 0
    req.decoded_text = ""
    req.surr_offset = None
    req.read_offset = None
    req.finished_reason = None
    req.finished_len = None
    req.finished_output = False
    req.sampling_params.max_new_tokens = decode_tokens
    req.cache_protected_len = 0

    # Switch to the target adapter BEFORE re-queueing; reset_for_retract and
    # the prefill scheduler read req.lora_id, so the re-prefill lands on the
    # target specialist. Set route_selected so this req is not re-routed once
    # it decodes again.
    old_lora_id = req.lora_id
    req.lora_id = target_lora_id
    req.lora_router_route_selected = route_id
    req.lora_router_route_decode_pending = False
    req.lora_router_kv_reuse = True
    req.skip_radix_cache_insert = True
    req.lora_router_stale_kv_end = 0

    # Stamp metadata so the response still reports the route decision. repeat=1
    # since this fills a single (reprefill) slot; the specialist decode rounds
    # that follow extend output_ids and get their own metadata via the normal
    # route hook only if re-routing were active -- it is not, because
    # lora_router_route_selected is already set.
    self._set_route_decode_metadata(
        req,
        route_id=route_id,
        adapter_name=adapter_name,
        decision=decision,
        router_text=router_text,
        router_tokens_removed=router_tokens_removed,
        prompt_tokens_removed=0,
        keep_prefix_token_count=original_prompt_tokens,
        decode_tokens=decode_tokens,
        deterministic_route=str(params.get("deterministic_route"))
        if params.get("deterministic_route") is not None else None,
        deterministic_route_decision=None,
    )

    # Release the KV this req holds (prefilled on the entry adapter); the
    # target adapter must recompute it. release_kv_cache is the mol-patched
    # version; with stale_kv_end==0 it falls through to the base tail, which
    # frees the req_to_token row + radix insert (skipped via the flag above).
    try:
        from sglang.srt.mem_cache.common import release_kv_cache as _release_kv_cache
        _release_kv_cache(req, self.tree_cache, is_insert=False)
    except Exception:
        logger.exception(
            "lora_router route_decode reprefill release_kv_cache failed rid=%s",
            req.rid,
        )

    # Mirror base sglang's release_req tail (release_req -> reset_for_retract).
    # reset_for_retract bumps retraction_count, clears prefix_indices/last_node,
    # zeroes kv_allocated_len/kv_committed_len, and sets is_retracted. It does
    # NOT touch origin_input_ids / lora_id / lora_router_* / sampling_params /
    # output_ids, so our restores above survive. Reusing it keeps us in lock
    # step with any base-side reset field additions across sglang patch versions.
    try:
        req.reset_for_retract()
    except Exception:
        logger.exception(
            "lora_router route_decode reprefill reset_for_retract failed rid=%s",
            req.rid,
        )
    # reset_for_retract set is_retracted=True; the prefill scheduler will clear
    # it during init_next_round_input. Keep our route flags as restored above.
    req.lora_router_route_selected = route_id
    req.lora_router_route_decode_pending = False

    # Drop the req from this decode batch (re-indexes every batch tensor via
    # filter_batch) and re-queue it for a fresh prefill on the next loop.
    keep_indices = [i for i in range(len(batch.reqs)) if i != index]
    batch.filter_batch(keep_indices=keep_indices)
    self._add_request_to_queue(req, is_retracted=True)
    # Ensure the running batch is not marked full so the reprefill is scheduled
    # promptly on the next loop.
    batch.batch_is_full = False

    logger.info(
        "lora_router route_decode reprefill request_id=%s route=%s adapter=%s "
        "old_lora=%s decision=%s prompt_tokens=%d removed_router_tokens=%d "
        "decode_tokens=%d (dropped from decode batch, re-prefilling on target)",
        req.rid, route_id, adapter_name, old_lora_id, decision,
        original_prompt_tokens, router_tokens_removed, decode_tokens,
    )
    return True


@_m
def _apply_route_decode_selection(
    self, req: Req, route_id: str, params: Dict[str, Any], decision: str
) -> int:
    original_prompt_tokens = len(req.origin_input_ids)
    router_tokens_removed = len(req.output_ids)
    router_text = self._decode_route_output_text(req)
    enable_kv_reuse = bool(params.get("enable_kv_reuse", True))
    keep_prefix_by_route = params.get("keep_prefix_token_count_by_route") or {}
    keep_prefix_token_count = 0
    if isinstance(keep_prefix_by_route, dict):
        try:
            keep_prefix_token_count = int(keep_prefix_by_route.get(route_id) or 0)
        except (TypeError, ValueError):
            keep_prefix_token_count = 0
    if keep_prefix_token_count <= 0:
        keep_prefix_token_count = int(params.get("keep_prefix_token_count") or 0)
    decode_tokens = int(params.get("decode_tokens") or 4)
    decode_tokens = max(0, decode_tokens)

    if keep_prefix_token_count <= 0:
        if enable_kv_reuse:
            keep_prefix_token_count = original_prompt_tokens
        else:
            # The non-reuse policy is normally executed by the harness as
            # target-adapter re-prefill. Keep a minimal scheduler anchor if
            # a caller still sends it through same-request route_decode.
            keep_prefix_token_count = min(1, original_prompt_tokens)
    # DIAG: force no-trim. Keeps the FULL routed prompt KV across the adapter
    # switch so the specialist continues with the complete context. If garble
    # disappears here, the trim/context-collapse is the cause; if it persists,
    # cross-adapter KV reuse is the cause.
    import os as _os
    if _os.environ.get("MOL_ROUTE_KEEP_FULL", "0") in ("1", "true", "True"):
        keep_prefix_token_count = original_prompt_tokens
    keep_prefix_token_count = min(keep_prefix_token_count, original_prompt_tokens)

    if keep_prefix_token_count < original_prompt_tokens:
        old_kv_allocated_len = req.kv_allocated_len
        if old_kv_allocated_len > keep_prefix_token_count:
            self._release_route_decode_trimmed_kv(
                req, keep_prefix_token_count, old_kv_allocated_len
            )
        req.origin_input_ids = array(
            "q", req.origin_input_ids[:keep_prefix_token_count]
        )
        req.origin_input_ids_unpadded = array(
            "q", req.origin_input_ids_unpadded[:keep_prefix_token_count]
        )
        req.cache_protected_len = 0
        req.kv_committed_len = min(req.kv_committed_len, keep_prefix_token_count)
        req.kv_allocated_len = min(req.kv_allocated_len, keep_prefix_token_count)

    del req.output_ids[:]
    req.fill_ids = array("q", req.origin_input_ids)
    req.send_token_offset = 0
    req.send_decode_id_offset = 0
    req.send_output_token_logprobs_offset = 0
    req.decoded_text = ""
    req.surr_offset = None
    req.read_offset = None
    req.finished_reason = None
    req.finished_len = None
    req.finished_output = False
    req.sampling_params.max_new_tokens = decode_tokens
    req.lora_router_route_decode_pending = False
    req.lora_router_route_selected = route_id

    target_lora_id, adapter_name = self._route_decode_target(
        route_id, params, req.lora_id
    )
    deterministic_route = params.get("deterministic_route")
    if deterministic_route is not None:
        deterministic_route = str(deterministic_route)
    deterministic_reason = params.get("deterministic_route_reason")
    deterministic_route_decision = None
    if isinstance(deterministic_reason, dict):
        deterministic_route_decision = deterministic_reason.get("decision")
        if deterministic_route_decision is not None:
            deterministic_route_decision = str(deterministic_route_decision)
    self._set_route_decode_metadata(
        req,
        route_id=route_id,
        adapter_name=adapter_name,
        decision=decision,
        router_text=router_text,
        router_tokens_removed=router_tokens_removed,
        prompt_tokens_removed=original_prompt_tokens - keep_prefix_token_count,
        keep_prefix_token_count=keep_prefix_token_count,
        decode_tokens=decode_tokens,
        deterministic_route=deterministic_route,
        deterministic_route_decision=deterministic_route_decision,
    )
    self._switch_req_lora(req, target_lora_id, "route_decode")
    logger.info(
        "lora_router route_decode selected request_id=%s route=%s adapter=%s "
        "decision=%s keep_prefix_tokens=%d removed_prompt_tokens=%d "
        "removed_router_tokens=%d decode_tokens=%d",
        req.rid,
        route_id,
        adapter_name,
        decision,
        keep_prefix_token_count,
        original_prompt_tokens - keep_prefix_token_count,
        router_tokens_removed,
        decode_tokens,
    )
    return keep_prefix_token_count

@_m
def _sync_route_decode_batch_state(
    self, batch: ScheduleBatch, index: int, req: Req, seq_len: int
) -> None:
    if batch.seq_lens is not None and index < len(batch.seq_lens):
        batch.seq_lens[index] = seq_len
    if batch.seq_lens_cpu is not None and index < len(batch.seq_lens_cpu):
        batch.seq_lens_cpu[index] = seq_len
    if getattr(batch, "orig_seq_lens", None) is not None and index < len(batch.orig_seq_lens):
        batch.orig_seq_lens[index] = seq_len
    if batch.input_ids is not None and index < len(batch.input_ids) and req.origin_input_ids:
        batch.input_ids[index] = int(req.origin_input_ids[-1])
    if batch.seq_lens_cpu is not None:
        try:
            batch.seq_lens_sum = int(batch.seq_lens_cpu.sum().item())
        except AttributeError:
            batch.seq_lens_sum = int(sum(batch.seq_lens_cpu))
    logger.info(
        "lora_router route_decode synced batch request_id=%s seq_len=%d",
        req.rid,
        seq_len,
    )

@_m
def _maybe_route_lora_route_decode(self, batch: ScheduleBatch) -> None:
    if self.lora_router_mode != "route_decode":
        return

    # Iterate over a snapshot of indices: the reprefill path calls
    # batch.filter_batch, which re-indexes batch.reqs in place. Snapshotting
    # by index is unsafe across a filter, so we re-resolve the req each pass
    # by rid and rebuild the live index list whenever the batch size changes.
    processed = set()
    while True:
        start_len = len(batch.reqs)
        progressed = False
        for index in range(start_len):
            if index >= len(batch.reqs):
                break  # batch shrank mid-scan due to a reprefill filter
            req = batch.reqs[index]
            rid = getattr(req, "rid", id(req))
            if rid in processed:
                continue
            if req.finished() or req.is_retracted:
                processed.add(rid)
                continue
            if getattr(req, "lora_router_route_selected", None):
                processed.add(rid)
                continue
            params = self._get_route_decode_params(req)
            if not params:
                processed.add(rid)
                continue
            if not getattr(req, "lora_router_route_decode_pending", False):
                req.lora_router_route_decode_pending = True
                self._mark_lora_reuse_request(req)
            if len(req.output_ids) == 0:
                processed.add(rid)
                continue

            router_text = self._decode_route_output_text(req)
            route_id = self._route_decode_deterministic_override(None, params)
            decision = "deterministic" if route_id is not None else "parsed"
            if route_id is None:
                route_id = self._parse_route_decode_output(router_text, params)
            router_max_tokens = int(params.get("router_max_tokens") or 16)
            if route_id is None and len(req.output_ids) >= router_max_tokens:
                route_id = str(params.get("base_route_id") or "base_model")
                decision = "fallback_base"
            if route_id is None:
                processed.add(rid)
                continue
            override_route_id = (
                None
                if decision == "deterministic"
                else self._route_decode_deterministic_override(route_id, params)
            )
            if override_route_id is not None:
                deterministic_reason = params.get("deterministic_route_reason")
                deterministic_decision = (
                    deterministic_reason.get("decision")
                    if isinstance(deterministic_reason, dict)
                    else deterministic_reason
                )
                logger.info(
                    "lora_router route_decode deterministic override request_id=%s "
                    "route=%s override=%s reason=%s",
                    req.rid,
                    route_id,
                    override_route_id,
                    deterministic_decision,
                )
                route_id = override_route_id
                decision = f"{decision}_deterministic_override"
            override_route_id = self._route_decode_signal_override(route_id, params)
            if override_route_id is not None:
                logger.info(
                    "lora_router route_decode signal override request_id=%s "
                    "route=%s override=%s",
                    req.rid,
                    route_id,
                    override_route_id,
                )
                route_id = override_route_id
                decision = f"{decision}_signal_override"

            # Resolve the target adapter up front so we can decide in-place vs
            # reprefill based on whether switching would co-locate >=2 distinct
            # specialists in this decode batch (the confirmed garble trigger).
            target_lora_id, adapter_name = self._route_decode_target(
                route_id, params, req.lora_id
            )

            if self._route_decode_would_cause_cospecialist(
                batch, index, req, target_lora_id
            ) and self._route_decode_reprefill_enabled():
                # Compromise fix: drop the req from this decode batch and
                # re-queue it for a fresh prefill on the target adapter so it
                # never decodes alongside a different specialist. This avoids
                # the per-slot weight_indices mis-map without touching base
                # sglang. The filter_batch inside re-indexes batch.reqs, so we
                # break out of this scan and re-snapshot the live batch.
                self._route_decode_reprefill_req(
                    batch, index, req, route_id, target_lora_id, adapter_name,
                    params, decision, router_text,
                )
                progressed = True
                break

            # In-place KV-reuse path (single specialist: safe, no garble).
            seq_len = self._apply_route_decode_selection(
                req, route_id, params, decision
            )
            self._sync_route_decode_batch_state(batch, index, req, seq_len)
            processed.add(rid)
            progressed = True

        # Re-loop only if a reprefill shrank the batch and there may still be
        # other pending reqs in the (re-indexed) batch to route this pass.
        if not progressed:
            break
        if len(batch.reqs) >= start_len:
            break

@_m
def _switch_req_lora(self, req: Req, new_lora_id: Optional[str], reason: str) -> bool:
    if req.finished() or req.is_retracted or req.lora_id == new_lora_id:
        return False
    old_lora_id = req.lora_id
    req.lora_id = new_lora_id
    self._mark_lora_reuse_request(req)
    logger.info(
        "lora_router %s switched request_id=%s old_lora=%s new_lora=%s "
        "prompt_tokens=%d output_tokens=%d skip_radix_cache_insert=%s",
        reason,
        req.rid,
        old_lora_id,
        new_lora_id,
        len(req.origin_input_ids),
        len(req.output_ids),
        req.skip_radix_cache_insert,
    )
    return True

@_m
def _maybe_route_lora_after_prefill(self, batch: ScheduleBatch) -> None:
    if self.lora_router_mode == "route_decode":
        self._maybe_route_lora_route_decode(batch)
        return
    if self.lora_router_mode not in (
        "prefill_decode",
        "prefill_decode_ba_interval",
    ):
        return
    if len(self.lora_router_pool) != 2:
        return

    for req in batch.reqs:
        if req.finished() or req.is_retracted:
            continue
        if len(req.output_ids) == 0:
            continue
        if getattr(req, "lora_router_prefill_switched", False):
            continue
        if req.lora_id != self.lora_router_pool[0]:
            continue
        req.lora_router_prefill_switched = True
        self._switch_req_lora(req, self.lora_router_pool[1], self.lora_router_mode)

@_m
def _maybe_route_lora_after_decode(self, batch: ScheduleBatch) -> None:
    if self.lora_router_mode == "route_decode":
        self._maybe_route_lora_route_decode(batch)
        return

    if self.lora_router_mode == "prefill_decode_ba_interval":
        if len(self.lora_router_pool) != 2:
            return
        n = self.lora_router_switch_every_n_tokens
        if n <= 0:
            return
        for req in batch.reqs:
            if req.finished() or req.is_retracted:
                continue
            output_tokens = len(req.output_ids)
            previous_bucket = getattr(req, "lora_router_decode_bucket", 0)
            current_bucket = output_tokens // n
            if current_bucket <= previous_bucket:
                continue
            req.lora_router_decode_bucket = current_bucket
            current = req.lora_id
            if current == self.lora_router_pool[0]:
                target = self.lora_router_pool[1]
            else:
                target = self.lora_router_pool[0]
            self._switch_req_lora(req, target, "prefill_decode_ba_interval")
        return

    if self.lora_router_mode != "token_interval":
        return
    n = self.lora_router_switch_every_n_tokens
    if n <= 0 or len(self.lora_router_pool) < 1:
        return
    for req in batch.reqs:
        if req.finished() or req.is_retracted:
            continue
        output_tokens = len(req.output_ids)
        previous_bucket = getattr(req, "lora_router_decode_bucket", 0)
        current_bucket = output_tokens // n
        if current_bucket <= previous_bucket:
            continue
        req.lora_router_decode_bucket = current_bucket
        if req.lora_id in self.lora_router_pool:
            index = self.lora_router_pool.index(req.lora_id)
            target = self.lora_router_pool[(index + 1) % len(self.lora_router_pool)]
        else:
            target = self.lora_router_pool[0]
        self._switch_req_lora(req, target, "token_interval")


def install(scheduler_cls):
    """Attach all extracted methods onto the given Scheduler class and resolve globals."""
    global Req, ScheduleBatch, logger
    import inspect
    import logging
    from sglang.srt.managers.schedule_batch import Req as _Req, ScheduleBatch as _SB
    Req = _Req
    ScheduleBatch = _SB
    # Reuse the scheduler module's logger so log lines land in the same stream.
    import sglang.srt.managers.scheduler as _sched
    logger = getattr(_sched, "logger", logging.getLogger("sglang.srt.managers.scheduler"))
    installed = []
    for fn in _METHODS:
        name = fn.__name__
        try:
            params = list(inspect.signature(fn).parameters)
        except (ValueError, TypeError):
            params = ["self"]
        # Methods whose first parameter is not `self` were @staticmethod in the
        # source; install them as staticmethods so calls like
        # self._strip_route_candidate(x) don't bind `self` to the first arg.
        if not params or params[0] != "self":
            setattr(scheduler_cls, name, staticmethod(fn))
        else:
            setattr(scheduler_cls, name, fn)
        installed.append(name)
    return installed
